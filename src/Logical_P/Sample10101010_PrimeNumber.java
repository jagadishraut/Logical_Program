package Logical_P;

public class Sample10101010_PrimeNumber
{
	public static void main(String[] args) 
	{
		int num=5;
		int count=0;
		
		for(int i=2; i<num; i++) 
		{
			count++;
			break;
		}
		
	
	if(count==1) 
	{
		System.out.println("given number is prime number");
	}
	else 
	{
		System.out.println("given number is not prime number");
	}
	}
}
