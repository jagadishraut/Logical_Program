package Logical_P;

public class Demo5_multiply_2_num_without_multi_operator
{
	public static void main(String[] args)
	{
		int num1=50;
		int num2=5;
		int sum=0;
		             // 1<=5
		for(int i=1; i<=num2; i++) 
		{
			sum=sum+num1;  //0+50=50, 50+50=100,
		}
		System.out.println(sum);
		
	}

}
